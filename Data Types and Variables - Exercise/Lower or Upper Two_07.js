function lowerOrUpperTwo(char){

    if(char === char.toLowerCase()){
        console.log('lower-case');
    }else if(char === char.toUpperCase()){
        console.log('upper-case');
        
    }
}
lowerOrUpperTwo('L');
lowerOrUpperTwo('f');
